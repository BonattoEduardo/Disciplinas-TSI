    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linha = tableProdutos.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(
                null,
                "Selecione um produto para remover!", 
                "Atenção", 
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        Produto produto = controller.getProdutos().get(linha);
        controller.remover(produto);
        atualizarTabela();
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
                int linha = tableProdutos.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(
                null,
                "Selecione um produto para alterar!", 
                "Atenção", 
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        Produto produto = controller.getProdutos().get(linha);
        controller.setProdutoSelecionado(produto);
        
        txtNome.setText(produto.getNome());
        txtPreco.setText(String.valueOf(produto.getPreco()));
        txtDescricao.setText(produto.getDescricao());
        estadoBotoes(false);
        estadoCampos(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

