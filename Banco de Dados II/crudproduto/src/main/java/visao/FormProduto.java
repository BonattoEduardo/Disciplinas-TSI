    private void atualizarTabela() {
        List<Produto> produtos = controller.atualizarProdutos();
        
        DefaultTableModel model = (DefaultTableModel) tableProdutos.getModel();
        model.setNumRows(0);
        
        produtos.forEach(produto -> {
            model.addRow(new Object[]{
                produto.getId(),
                produto.getNome(),
                produto.getPreco(),
                produto.getDescricao()
            });
        });
    }
    
    public final void estadoBotoes(boolean status) {
        btnSalvar.setEnabled(!status);
        btnCancelar.setEnabled(!status);
        btnCadastrar.setEnabled(status);
        btnAlterar.setEnabled(status);
        btnRemover.setEnabled(status);
    }
    
    public final void estadoCampos(boolean status) {
        txtNome.setEnabled(status);
        txtPreco.setEnabled(status);
        txtDescricao.setEnabled(status);
    }
    
    public void limparCampos() {
        controller.setProdutoSelecionado(null);
        txtNome.setText("");
        txtPreco.setText("");
        txtDescricao.setText("");
    }

